class RelationalOperator{

	public static void main(String args[])
	{
		int var1 = 80;
		int var2 = 10;
		
		System.out.println(var1 == var2);
		System.out.println(var1 != var2);
		System.out.println(var1 > var2);
		System.out.println(var1 >= var2);
		System.out.println(var1 < var2);
		System.out.println(var1 <= var2);
		
		
		
	}
}